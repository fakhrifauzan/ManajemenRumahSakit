    public void viewDokter(ArrayList<Dokter> dataDokter){
        String[] title = {"ID", "Nama", "Gender", "Alamat", "Spesialis"};
        Object data[][] = new Object[dataDokter.size()][5];
        int i = 0;
        for (Dokter d : dataDokter) {
            data[i][0] = (String) d.getId_dokter();
            data[i][1] = (String) d.getNama();
            data[i][2] = (String) d.getJenisKelamin();
            data[i][3] = (String) d.getAlamat();
            data[i][4] = (String) d.getSpesialis();
            i++;
        }
        tblDokter.setModel(new DefaultTableModel(data, title));
    }
    
